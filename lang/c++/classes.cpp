//------------------------------------------------------------------------------
#include <iostream>

//------------------------------------------------------------------------------
class Base {
public:
    virtual void display( int p = 1 ){
        std::cout << "Base = " << p << std::endl;
    }
};

//------------------------------------------------------------------------------
class Derived : public Base {
public:
    virtual void display( int p = 2 ){
        std::cout << "Derived = " << p << std::endl;
    }
};

//------------------------------------------------------------------------------
void test1(){
    Base * pbb = new Base();
    Derived * pdd = new Derived();
    Base * pbd = new Derived();

    pbb->display();
    pdd->display();
    pbd->display();    
}

//------------------------------------------------------------------------------
int main(){
    test1();
}

